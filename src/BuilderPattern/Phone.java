package BuilderPattern;
public class Phone{
    String os;
    String processor;
    double screenSize;
    int battery;
    int camera;
    public Phone(String os,String processor,double screenSize,int battery,int camera){
        super();
        this.os=os;
        this.processor=processor;
        this.screenSize=screenSize;
        this.battery=battery;
        this.camera=camera;
    }
    
}